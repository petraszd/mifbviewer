#include <QApplication>
#include <QGLFormat>
#include "mainwindow.h"

int main(int argc, char *argv[])
{
    QGLFormat fmt;
    fmt.setSampleBuffers(true);
    QGLFormat::setDefaultFormat(fmt);

    QApplication a(argc, argv);
    MainWindow w;
    w.show();
    return a.exec();
}
